package Beans;

/**
 *
 * @author michi
 */
public class Position
{
    private int x;
    private int y;

    public Position(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public void increaseX(int value)
    {
        x += value;
    }
    public void increaseY(int value)
    {
        y += value;
    }
    public int getX()
    {
        return x;
    }

    public void setX(int x)
    {
        this.x = x;
    }

    public int getY()
    {
        return y;
    }

    public void setY(int y)
    {
        this.y = y;
    }
    
    
            
            
}
